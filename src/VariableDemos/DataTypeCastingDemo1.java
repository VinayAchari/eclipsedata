package VariableDemos;

public class DataTypeCastingDemo1 
{

	public static void main(String[] args) 
	{ int a=10;
	float f1 =10;
	//float f1=a;implicit Data Typescasting
	System.out.println(f1);	
	
	long k=30000;
	float f2=k;//implicit Data Typescasting
	System.out.println(f2);
	
	double d1=a;
	System.out.println(d1);//implicit Data Typescasting
	
	double d2=k;
	System.out.println(d2);//implicit Data Typescasting
	
	double d3=k;
	System.out.println(d3);//implicit Data Typescasting

	}

}
