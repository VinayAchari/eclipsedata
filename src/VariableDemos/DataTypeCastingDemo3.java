package VariableDemos;

public class DataTypeCastingDemo3 
{

	public static void main(String[] args) 
	{
		int a=(int)10.23;
		System.out.println(a);
		
		float f1 =(float)10.2366978;
		System.out.println(f1);
		
		float f2 =(float)10.0;
		System.out.println(f2);
		
		long k =(long)10.23;
		System.out.println(k);
		

	}

}
